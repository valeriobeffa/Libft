#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		x;

	x = 0;
	while (s[x])
	{
		if ((char)c == s[x])
			return ((char *)(s + x));
		x++;
	}
	if ((char)c == '\0')
		return ((char *)s + x);
	return (NULL);
}
