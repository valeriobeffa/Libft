int	ft_atoi(const char *str)
{
	int	x;
	int	y;
	int	len;

	x = 1;
	y = 0;
	len = 0;
	while (*str == 32 || (*str <= 13 && *str >= 9))
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
			x = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9' && len++ > -1)
	{
		y = y * 10 + (*str - '0');
		str++;
	}
	if (y > -2147483648 && y < 2147483647 && len > 18 && x == 1)
		return (-1);
	if (y > -2147483648 && y < 2147483647 && len > 18 && x == -1)
		return (0);
	return (x * y);
}
